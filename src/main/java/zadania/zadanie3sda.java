package zadania;

public class zadanie3sda {
        public static void sumRange(int x, int y) {
            int suma = 0;
            for (int i = x; i <= y; i++) {
                suma += i;
            }
            System.out.println("Zadanie3: Suma liczb " + x + " to " + y + " = " + suma);
        }

    }

